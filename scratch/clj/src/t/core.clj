(defn attack
  [crit-range to-hit ac roll dmg]
  (/ (->> (range 20)
          (map inc)
          (map (fn [n] (cond (>= n crit-range)
                             (+ (roll 10) (roll 10)
                                (roll 4) (roll 4)
                                (roll 4) (roll 4)
                                dmg)
                             (>= (+ n to-hit) ac)
                             (+ (roll 10)
                                (roll 4)
                                (roll 4)
                                dmg)
                             :miss
                             0)))
          (reduce +))
     20))

